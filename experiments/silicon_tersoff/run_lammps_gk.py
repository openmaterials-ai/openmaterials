"""LAMMPS Green-Kubo κ for silicon-Tersoff at 300 K (phase 2 P4).

Operator-layer pedigree:
  contract_kappa[transport_model=green_kubo]
  → omai.thermal_transport.representation.lammps.LAMMPS_CONTRACT_KAPPA_GREEN_KUBO
  → this driver
  → out/kappa_lammps_gk.npy

Sympy formula being exercised (from the operator edge):

    κ^{αβ}_GK = V/(k_B T²) · ∫₀^{τ_max} ⟨J^α(0) J^β(τ)⟩ dτ

LAMMPS pipeline:
  - velocity-Verlet on a Si-Tersoff supercell (default 4×4×4 = 512 atoms);
  - nvt/temp 300 K equilibration for `n_equil` steps;
  - nve production for `n_prod` steps, with `compute heat/flux` consuming
    per-atom KE/PE/stress and `fix ave/correlate` writing J0Jt.dat;
  - the driver parses J0Jt.dat, computes V/(k_B T²) · numpy.trapz(...) per
    αβ component, and writes the 3×3 κ tensor to out/kappa_lammps_gk.npy.

Behaviour with no LAMMPS:
  - Generates the input script `out/in.silicon_gk` and the seed structure
    `out/data.silicon`.
  - Prints "lmp not on PATH; run script with: lmp -in out/in.silicon_gk".
  - Exits 0 (this is *expected* on a CI host without LAMMPS, not a failure).
"""

from __future__ import annotations

import shutil
import subprocess
import sys
from pathlib import Path

import numpy as np
from ase.io import write as ase_write

from seed import (
    LATTICE_CONSTANT,
    RUNS_DIR,
    TEMPERATURE,
    TERSOFF_FILE,
    build_silicon_primitive,
)

# ---------------------------------------------------------------------------
# Output dir + run parameters
# ---------------------------------------------------------------------------

OUT = RUNS_DIR / "lammps_gk"
OUT.mkdir(parents=True, exist_ok=True)

SUPERCELL_GK = (4, 4, 4)            # ~512 atoms — small but enough for κ_GK
DT_FS = 0.5                         # MD time step in fs (canonical for Si)
N_EQUIL = 50_000                    # 25 ps NVT equilibration
N_PROD = 1_000_000                  # 500 ps NVE production
SAMPLE_EVERY = 5                    # accumulate J every 5 steps
CORR_DEPTH = 2000                   # correlation depth (lag points)
DUMP_EVERY = 1000                   # log J0Jt every 1000 steps

# kappa accumulator unit: LAMMPS metal units output J in (eV * Å / fs / Å³)
# = eV/(Å²·fs). With κ = V/(k_B T²) · ∫⟨JJ⟩ dτ and τ in fs, V in Å³,
# k_B in eV/K, the resulting κ is in eV/(K · Å · fs). Multiplying by
# 1.602e-19 (eV→J) and 1e15 (1/fs → 1/s) and dividing by 1e-10 (Å→m) gives
# W/(m·K). Worked out below in `gk_kappa_units_to_W_per_mK`.


def gk_kappa_units_to_W_per_mK(kappa_metal: np.ndarray) -> np.ndarray:
    """Convert κ from LAMMPS-metal-Green-Kubo units to W/(m·K).

    The metal-units chain: J in eV/(Å²·fs), τ in fs, V in Å³, k_B in eV/K
    yields κ in eV/(K·Å·fs). Multiply by:

        eV → J         : 1.602176634e-19
        1/fs → 1/s     : 1e15
        Å → m          : 1e-10  (in the denominator, so multiply by 1/1e-10 = 1e10)

    Net factor: 1.602176634e-19 * 1e15 * 1e10 = 1.602176634e+6.
    """
    return kappa_metal * 1.602176634e6


def write_lammps_data(path: Path) -> None:
    """Write the supercell as LAMMPS data file."""
    primitive = build_silicon_primitive()
    supercell = primitive * SUPERCELL_GK
    ase_write(str(path), supercell, format="lammps-data", atom_style="atomic")


def write_input_script(path: Path, data_file: Path, tersoff_file: Path,
                       jjt_file: Path) -> None:
    """Write the LAMMPS input script for the Green-Kubo run."""
    script = f"""# LAMMPS Green-Kubo κ for Si-Tersoff at {TEMPERATURE} K.
# Generated by experiments/silicon_tersoff/run_lammps_gk.py
# Phase 2 P4 — wires contract_kappa[transport_model=green_kubo] to a real
# LAMMPS run via the LAMMPS_CONTRACT_KAPPA_GREEN_KUBO adapter spec.

units           metal
atom_style      atomic
boundary        p p p

read_data       {data_file}

pair_style      tersoff
pair_coeff      * * {tersoff_file} Si

mass            1 28.0855

timestep        {DT_FS * 1e-3}  # metal units = ps; 0.5 fs = 5e-4 ps

# --- equilibration: NVT at T -----------------------------------------------
velocity        all create {TEMPERATURE} 4928459 mom yes rot yes dist gaussian
fix             nvt all nvt temp {TEMPERATURE} {TEMPERATURE} 0.1
run             {N_EQUIL}
unfix           nvt

# --- production: NVE with heat-flux autocorrelation ------------------------
fix             nve all nve

compute         ke all ke/atom
compute         pe all pe/atom
compute         stress all stress/atom NULL virial
compute         heatflux all heat/flux ke pe stress

# fix ave/correlate writes < J_alpha(0) J_beta(tau) > to J0Jt.dat.
# We request all 9 pair correlations (full αβ tensor).
fix             jjt all ave/correlate {SAMPLE_EVERY} {CORR_DEPTH} {DUMP_EVERY} &
                c_heatflux[1] c_heatflux[2] c_heatflux[3] &
                type full file {jjt_file}

# Thermo output for sanity-checking.
thermo          5000
thermo_style    custom step temp press pe ke etotal vol

run             {N_PROD}
"""
    path.write_text(script)


def parse_jjt(jjt_path: Path):
    """Parse LAMMPS `fix ave/correlate` output (type full).

    The file format is described at
    https://docs.lammps.org/fix_ave_correlate.html — a header block per
    sampling window, then `Nrepeat` lines per window with columns:

        index  TimeDelta  Ncount  v_1*v_1  v_1*v_2  v_1*v_3 \
                                  v_2*v_1  v_2*v_2  v_2*v_3 \
                                  v_3*v_1  v_3*v_2  v_3*v_3

    We average all windows together and return:

        dt_ps   — time-delta array (ps)
        Jcorr   — (N_lag, 3, 3) tensor

    Robust to multiple sampling windows: averages them.
    """
    text = jjt_path.read_text().splitlines()
    blocks = []
    cur = []
    for line in text:
        if line.startswith("#"):
            continue
        parts = line.split()
        if len(parts) == 2:
            # Window header: TimeOrigin Nrepeat
            if cur:
                blocks.append(cur)
            cur = []
            continue
        if len(parts) >= 12 and not line.startswith("#"):
            cur.append([float(x) for x in parts])
    if cur:
        blocks.append(cur)

    if not blocks:
        raise RuntimeError(f"no correlation windows parsed from {jjt_path}")

    # blocks: list of windows, each a list of N_lag rows of 12 cols
    # (idx, TimeDelta, Ncount, 9 cross-correlations).
    arrs = [np.asarray(b) for b in blocks if len(b) >= CORR_DEPTH]
    if not arrs:
        raise RuntimeError(
            f"no complete correlation windows in {jjt_path} (need depth {CORR_DEPTH})"
        )
    stacked = np.stack(arrs, axis=0)   # (n_windows, n_lag, 12)
    averaged = stacked.mean(axis=0)    # (n_lag, 12)

    dt_ps = averaged[:, 1] * (DT_FS * 1e-3)  # LAMMPS reports lag in *steps*
    raw9 = averaged[:, 3:12].reshape(-1, 3, 3)
    return dt_ps, raw9


def kappa_from_jjt(dt_ps: np.ndarray, Jcorr: np.ndarray,
                    volume_A3: float, temperature_K: float) -> np.ndarray:
    """Integrate Jcorr_αβ(τ) over τ and apply the V/(k_B T²) prefactor.

    Returns κ in W/(m·K).
    """
    k_B_eV_per_K = 8.617333262e-5
    # numpy.trapz integrates along axis 0 (τ) by default; we want a 3x3
    # output, so do component-by-component.
    integrated = np.zeros((3, 3))
    for a in range(3):
        for b in range(3):
            integrated[a, b] = np.trapz(Jcorr[:, a, b], dt_ps)
    prefactor = volume_A3 / (k_B_eV_per_K * temperature_K**2)
    kappa_metal = prefactor * integrated
    return gk_kappa_units_to_W_per_mK(kappa_metal)


def main() -> int:
    data_file = OUT / "data.silicon"
    input_script = OUT / "in.silicon_gk"
    jjt_file = OUT / "J0Jt.dat"
    kappa_file = OUT / "kappa_lammps_gk.npy"

    print(f"[run_lammps_gk] writing LAMMPS data file: {data_file}")
    write_lammps_data(data_file)
    print(f"[run_lammps_gk] writing LAMMPS input script: {input_script}")
    write_input_script(input_script, data_file, TERSOFF_FILE, jjt_file)

    # Locate LAMMPS binary.
    candidates = ["lmp", "lmp_serial", "lmp_mpi", "lammps"]
    lmp_bin = next((shutil.which(c) for c in candidates if shutil.which(c)), None)
    if lmp_bin is None:
        print(f"[run_lammps_gk] no LAMMPS binary on PATH "
              f"(tried: {candidates}).")
        print(f"[run_lammps_gk] to produce κ_GK, run:")
        print(f"  cd {OUT}")
        print(f"  lmp -in {input_script.name}")
        print(f"  python {__file__} --parse-only")
        print(f"[run_lammps_gk] exiting cleanly (no work done).")
        return 0

    if "--parse-only" not in sys.argv:
        print(f"[run_lammps_gk] running LAMMPS: {lmp_bin} -in {input_script}")
        result = subprocess.run(
            [lmp_bin, "-in", str(input_script)],
            cwd=str(OUT),
            check=False,
        )
        if result.returncode != 0:
            print(f"[run_lammps_gk] LAMMPS exited with {result.returncode}")
            return result.returncode

    if not jjt_file.exists():
        print(f"[run_lammps_gk] no {jjt_file} produced — nothing to parse.")
        return 1

    print(f"[run_lammps_gk] parsing {jjt_file}")
    dt_ps, Jcorr = parse_jjt(jjt_file)

    # Reload supercell to know volume.
    primitive = build_silicon_primitive()
    supercell = primitive * SUPERCELL_GK
    volume_A3 = supercell.get_volume()

    print(f"[run_lammps_gk] computing κ_GK = V/(k_B T²) · ∫⟨JJ⟩ dτ")
    print(f"  supercell volume : {volume_A3:.2f} Å³ ({len(supercell)} atoms)")
    print(f"  τ_max            : {dt_ps[-1]:.3f} ps ({len(dt_ps)} lag points)")
    print(f"  T                : {TEMPERATURE} K")

    kappa = kappa_from_jjt(dt_ps, Jcorr, volume_A3, TEMPERATURE)
    np.save(kappa_file, kappa)
    print(f"[run_lammps_gk] wrote {kappa_file}")
    print(f"  κ_xx, κ_yy, κ_zz (W/m·K): "
          f"{kappa[0, 0]:.1f}  {kappa[1, 1]:.1f}  {kappa[2, 2]:.1f}")
    print(f"  diagonal mean              : "
          f"{(kappa[0, 0] + kappa[1, 1] + kappa[2, 2]) / 3:.1f}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
